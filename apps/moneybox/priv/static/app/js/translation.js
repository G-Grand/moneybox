String.prototype.contains = function (segment) { return this.indexOf(segment) !== -1; };
String.prototype.endsWith = function(suffix) {
    return this.indexOf(suffix, this.length - suffix.length) !== -1;
};

function i18n(word) {
    var translation = locale[currentLanguage()][word];
    return (translation == null) ? word : translation; }

function detectLanguage() {
       if (window.location.search.contains("locale=en")||
           window.location.search.contains("locale=EN")||
           window.location.pathname.contains("/EN/")) lang = 'en';
  else if (window.location.search.contains("locale=ru")||
           window.location.search.contains("locale=RU")||
           window.location.pathname.contains("/RU/")) lang = "ru"; }

function currentLanguage() { return lang; }
